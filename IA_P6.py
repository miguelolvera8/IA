#para concatenar operaciones matematicas se usa la coma en vez de la +
suma = 28 + 20 + 30                         #para hacer la suma hacemos lo siguiente
resta = -100 + 30 + 60                      #para hacer la resta hacemos lo siguiente
mul = 512 * 2                               #para la multiplicacion hacemos el uso de *
div = 20 / 2                                #para la division /
op = 50 + 5 - 15 / 3                        #aqui usamos varios operadores
print(suma, resta, mul, div, op,)           #y mostramos separando por coma